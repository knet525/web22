						<h4>Yamunastak : Its Meaning & Mahatmya</h4>
						
						<p>
						Shri Yamuna is the fourth element of Shrinathji. Shri Yamuna is the daughter of Surya (Sun) and sister of Yam. She is symbol of Mukti from the evils of Yam and light. Yamunaji is the “ISHT DEVI” of Pushtimarg.</p>
						<p>
						Her another name is Kalindi also. She is also the daughter of Surya. She is the fourth patrani of Krishna but Yamunaji the daughter of Surya is beloved of Shri Krishna and not the wedded wife. She is the fourth Swamini.</p>
<p>
Yamunaji had emerged from the heart of Surya Narayan and landed at Mount Kalindi. She had flown on the earth for the benefit of its disciples to have Bhakti Bhav.</p>

<p>Yamunaji is the melted form of Bhakti Rass liquified image. She has three images.Mythologically she carries garland of lotus in her hands for Lord Krishna. Religious form of Yamunaji is visible to all. Mythological image can only be known through experience. Shri Vallabhacharya has described this Yamunastak.
She is sister of “Niyamak” who keeps account of human beings good and bad deeds. He who takes water of Yamuna becomes the son of Yamuna. Hence he who takes bath in Yamuna should not be afraid of Yam.</p>

<div class="separator-2"></div>


<p><h3>Shree Yamunashtakam</h3></p>


<p>In V.S. 1549 (year 1493) at the age of 14 years, Shri Mahaprabhuji arrived in Gokul during his first global tour; he resided at Thakurani Ghat / Govind ghat on the bank of Yamuna river. At this time he formed Shri Yamunastakam which describes Shri Yamunaji’s divine description. In Yamunastakam’s first eight shloks, Shri Mahaprabhuji describes Shri Yamunaji’s eight fold powers, its divine & wonderful swarup and her divine qualities. Shri Yamunaji is the daughter of divine Sun. Her purpose for coming to earth from heaven through Kalind mountain is to bless her devotees. Her physical state as a river is also very attractive and charming. Shri Yamunaji blesses her devotees with eight fold powers. </p>

<p>To cite an example from Shri Mahaprabhuji's 84 Vaishnavs, Kishoribai, who was chanting only two lines of Yamnuastak with utmost devotion and faith was blessed with all divine fruits. </p>

<p><strong>So Let us all Pray to Shri Yamuna Maharani to bless us with Bhakti Raas </p></strong>


<p align="center">********** Shlok 1 **********</p>
<p><strong>नमामि यमुनामहं सकल सिद्धि हेतुं मुदा
मुरारि पद पंकज स्फ़ुरदमन्द रेणुत्कटाम ।<br>
तटस्थ नव कानन प्रकटमोद पुष्पाम्बुना
सुरासुरसुपूजित स्मरपितुः श्रियं बिभ्रतीम ॥१॥</p></strong>

<p>Gujarati<br>
Shri krishna na charanarvind ni raj thaki shobhi rahya, siddhi alaukik aapnara vundu shri yamunajine, Supushpa ni suvas thi jungle badhu meheki rahyu ne mand shital pavan thi jal pan sugandhit thai rahyu, Puje surasur sneh thi vali sevata daivi jivo, Vandan karu shri yamunajine shri krishna aashraya aapjo

<p>Explanation<br>
I (shri mahaprabhuji) bow (Namami) to Shri Yamunaji with my full love and devotion. She is capable of giving fulfillment of all the desires(Sakal Siddhi) of the devotee in enjoying God’s love. Shri Yamunaji is capable of giving all kind of achievements. This first shlok describes the physical state of the river. Its both banks are full of glittering sand which is believed to be as soft as lotus feet of Shri Murari Prabhu (Shri Krishna.) Also, there are various kind of EVERGREEN gardens of the both banks of the river. The river water is full of scent due to flowers of the garden. Shri Yamunaji is also worshipped by two kinds(Suraa -Sur)of vraj devotees.</p>


<p align="center">********** Shlok 2 **********</p>

<p><strong>कलिन्द गिरि मस्तके पतदमन्दपूरोज्ज्वला विलासगमनोल्लसत्प्रकटगण्ड्शैलोन्न्ता ।<br>
सघोषगति दन्तुरा समधिरूढदोलोत्तमा मुकुन्दरतिवर्द्धिनी जयति पद्मबन्धोः सुता ॥२॥</strong></p>

<p>Gujarati<br>
Maa suryamandal chodine bahu veg thi aavi rahya, Tya kalindi na shikhar upar shobha ati sundar dise, Ae veg ma patthar ghana harkhaine uchali rahya ne aap pan ullas purvak uchalata shobhi rahya, Hari het na jula upar jane birajya aap ho, Vandan karu shri yamunajine shri krishna aashraya aapjo</p>

<p>Explanation<br>
In this shlok, Shri Mahaprabhuji describes that how Shri Yamunaji can bless her devotees to increase their love towards Shri Mukund Prabhu. Also, Shri Mahaprabhuji describes the physical existence of Yamunaji as river. It flows enthusiastically (With speed and splendour Poorojjwalaa) from the top of Kalind mountain. The water appears to be as milk because of the force and twirls. It seems like Shri Yamunaji is very eager to go in Vraj and meet Shri Krishna. It also gives impression as Shri Yamunaji is swinging of (Dolottama) jhula. Shri Yamunaji is a daughter of Sun, who is the friend of lotus(Padma).</p>



<p align="center">********** Shlok 3 **********</p>

<p><strong>भुवं भुवनपावनीमधिगतामनेकस्वनैः प्रियाभिरिव सेवितां शुकमयूरहंसादिभिः ।<br>
तरंगभुजकंकण प्रकटमुक्तिकावाकुकानितन्बतटसुन्दरीं नमत कृष्ण्तुर्यप्रियाम ॥३॥</strong></p>

<p>Gujarati<br>
Shuk mor saras hans aadi pakshi thi sevayela, gopijano ae sevya bhuvan svajan pavan rakhta Tarang rup shri hast ma reti rupi moti tana, kankan saras shobhi rahya shri krishna ne bahu priya je Nitamb rup shri tat tanu adbhut darshan thay jo Vandan karu shri yamunajine shri krishna aashraya aapjo </p></p>
<p>Explanation<br>
Whenever Shri Yamunaji comes to earth from heaven, she blessed the Earth (Bhuvam Bhuvana Pawaneem). All birds like Parrot, Peacock, Hansas (Swans) serves Shri Yamunaji. Shri Mahaprabhuji visualize glittering (sterling) sand as her bangle’s pearls (muktikavaluka) and waves of water as beautiful divine hands (Tarang Bhuj Kankana) of Shri Yamunaji. From the birds eye view, Shri Yamunaji appears as beautiful with both the banks with glamourous sand and river full of water. She is favorite fourth queen of Shri Krishna. And Shri Mahaprabhuji asks us to bow to her (Namat Krishna Turya Priyam).</p>



<p align="center">********** Shlok 4 **********</p>

<p><strong>अनन्तगुण भूषिते शिवविरंचिदेवस्तुतेघनाघननिभे सदा ध्रुवपराशराभीष्टदे ।<br>
विशुद्ध मथुरातटे सकलगोपगोपीवृतेकृपाजलधिसंश्रिते मम मनः सुखं भावय ॥४॥</strong></p>

<p>Gujarati<br>
Anant gun thi shobhata stuti dev brahma shiv kare, ghanashyam jevu megh sam che swarup sundar aapnu Vishuddh mathura aapna sannidhya ma shobhi rahyu, sahu gop gopi vrund ne icchit fal aapi rahya Mam kod sau pura karo jyam dhruv parashar na karya Vandan karu shri yamunajine shri krishna aashraya aapjo</p>

<P>Explanation<br>
In this shlok, Shri Mahaprabhuji describes the prayer to Shri Yamuna Maharani. O! Shri Yamunaji, you are having infinite divine qualities – countless virtues. Even, Gods like Shiv, Brahama praises you. You are capable of granting wishes to devotees like Dhruv and Parasher. There are holy pilgrim places like “Mathura” on your banks. You are always surrounded with gopi’s and gopijans and you are always protected by blessings of Shri Krishna. O! Shri Yamunaji, I wish that you give me such a blessing that it gives peace and happiness to my mind. Chanting this shlok with pure and honest heart gives tremendous mental peace and eternal happiness. (Kishori bai was chanting only two lines Vishuddha Mathura Tate, Sakal Gopgopi Vrute, Krupa Jaladhi Sanshrite, Mama Manha Sukham Bhavayah. with full faith. Shri Yamunaji blessed Kishoribai with all divine fruits.) </p>



<p align="center">********** Shlok 5 ********** </p>

<p><strong>यया चरणपद्मजा मुररिपोः प्रियं भावुकासमागमनतो भवत्सकलसिद्धिदा सेवताम ।<br>
तया सह्शतामियात्कमलजा सपत्नीवयहरिप्रियकलिन्दया मनसि मे सदा स्थीयताम ॥५॥ </p></strong>

<P>Gujarati<br>
Shri krishna na charano thaki shri jahnavi utpanna thaya, satsang pamya aapno ne siddhidayak thai gaya Evu mahatmya che aapnu sarakhamani koi shu kare, samakaksha ma aavi shake sagarsuta ek j khare Eva prabhu ne priya mara hradaya ma aavi vaso Vandan karu shri yamunajine shri krishna aashraya aapjo </p>
<P>Explanation<br>
Shri Gangaji evolved from the lotus feet of Shri Bhagavan. Shri Gangaji became holy and pious due to merging with Shri Yamunaji at Triveni Sangam. Shri Yamunaji made Shri Gangaji capable of giving all achievements Sakal Siddhi to the serving devotees. Only Shri Laxmiji is equal and comparable to Shri Yamu
naji-but on a lesser degree. I wish that this type of Shri Yamunaji, who can take all worries of her devotees and who is also favorite (Hari Priya) to Shri Krishna, come and stayput in my heart and soul forever. </p>



<p align="center">********** Shlok 6 ********** </p>

<p><strong>नमोस्तु यमुने सदा तव चरित्र मत्यद्भुतंन जातु यमयातना भवति ते पयः पानतः ।<br>
यमोपि भगिनीसुतान कथमुहन्ति दुष्टानपिप्रियो भवति सेवनात्तव हरेर्यथा गोपिकाः ॥६॥</strong></p>

<P>Gujarati<br>
Adbhut charitrya che aapnu vandan karu hu prem thi, yamayatana aave nahi ma aapana payapan thi Kadi dushta hoiye toy pan santan chiye ame aapna, sparshe na amne koi bhaya chaya sada che aapni Gopi janone prabhu priya banya evi krupa bas rakhajo Vandan karu shri yamunajine shri krishna aashraya aapjo </p>
<P>Explanation<br>
O! Shri Yamunaji, I bow to you with my full body and heart. Your divine character is very wonderful. Just by drinking your water in Vraj, we do not have to worry about painful death. Your devotees can become favorite to Shri Krishna just like gopijanas who became favorite by doing “Katyani Vrat”. In winter (GOPMAS) Vrat means fasting. </p>



<p align="center">********** Shlok 7 ********** </p>

<p><strong>ममास्तु तव सन्निधौ तनुनवत्वमेतावतान दुर्लभतमारतिर्मुररिपौ मुकुन्दप्रिये ।<br>
अतोस्तु तव लालना सुरधुनी परं सुंगमा-त्तवैव भुवि कीर्तिता न तु कदापि पुष्टिस्थितैः ॥७॥</strong></p>

<P>Gujarati<br>
Shri krishna ne priya aap cho mamadeh sundar rakhajo, bhagavad lila ma thay priti sneh evo rakhjo Jyam aapna sansarg thi gangaji pushti ma vahya, mamadeha man shri krishna ne priya thay eva rakhajo Viraharti ma he mat mara hradaya ma birajajo Vandan karu shri yamunajine shri krishna aashraya aapjo </p>
<p>Explanation<br>
O! Shri Yamunaji, who is favorite of Shri Mukund Prabhu, I pray to you to give me divine bodyTanu Navatv which can be used in divine Leela of LORD and also to serve you. Only you are capable of doing so. With such a divine body, I can serve Shri Mukund Prabhu very well. Pushti Margiya vaishnavas -Divine souls (Jeevs) had never praised Shri Gangaji without your merging with Shri Gangaji. </p>



<p align="center">********** Shlok 8 ********** </p>

<p><strong>स्तुति तव करोति कः कमलजासपत्नि प्रियेहरेर्यदनुसेवया भवति सौख्यमामोक्षतः ।<br>
इयं तव कथाधिका सकल गोपिका संगम-स्मरश्रमजलाणुभिः सकल गात्रजैः संगमः ॥८॥</strong></p>

<p>Gujarati<br>
Hu aapani stuti shu karu mahatmya aparampar che, shri laxmi vishnu sevavathi mox no adhikar che Pan aani seva thaki adbhut jalkrida tana, jal na anu ni prapti thaye gopijano na sneha thi Ae sneh nu sukh divya che man maru ema sthapajo Vandan karu shri yamunajine shri krishna aashraya aapjo </p>
<p>Explanation<br>
O! Shri Yamunaji, you are like Shri Laxmiji, who has good fortune. But no one is capable of praising you, because any one serving God first and then Shri Laxmiji gets all happiness after death also Mamoxataha. But the importance of Shri Yamunaji is the best. Here Shri Maha Prabhuji explains the exaltation of soul just by bathing in the waters of shri yamunaji which becomes pious daily by the “Ras-Lila of Lord with the vraj-bhakts”. By serving Shri Yamunaji we get blessing from her, which is helpful in exalting our soul and making pious. Due to her blessings our mind and thoughts becomes favorable to serve Shri Krishna. </p>



<p align="center">********** Shlok 9 **********</p>

<p><strong>तवाष्टकमिदं मुदा पठति सूरसूते सदासमस्तदुरितक्षयो भवति वै मुकुन्दे रतिः ।<br>
तया सकलसिद्धयो मुररिपुश्च सन्तुष्यतिस्वभावविजयो भवेत वदति वल्लभः श्री हरेः ॥९॥</strong></p>

<p>Gujarati<br>
Koi sneha thi karashe sada aa path yamunashtaka tano, nishche prabhu ne priya thashe ne nash thashe pap no Siddhi sakala malashe ane shri krishna ma vadhashe priti, anand sagar umatashe ne svabhava pan jashe jiti Jagadish ne vahala amara vallabhadhish ucchare Vandan karu shri yamunajine shri krishna aashraya aapjo</p>
<p>Explanation<br>
O! Shri Yamunaji! Whoever always sings these hymns of Shri Yamunastak with happiness-joy and pleasure, will get following fruits.
<ul class="list-unstyled">
<li> Absolving one of all the worldly sins.</li>
<li> The love for the lord increases manifold.</li>
<li> All yours endeavours, worldly and spiritual, are completed succesfully.</li>
<li> You become a worthy recepient of God’s grace-love.</li>
<li> You emerge victorious over your nature. Sakal Siddhi (”ashta -eight” siddhis)</li>
<p><ul>
      <li>Body to worship God.</li>
      <li>To watch God’s action.</li>
      <li>To feel God’s action.</li>
      <li>To get sarvatm Bhav siddhi.</li>
      <li>To remain and have God’s blessing even during difficult days.</li>
      <li>To get divine sight (Darshan of lord).</li>
      <li>To feel the pleasure of God’s blessings (Krupa).</li>
      <li>To feel God’s presence in all situations.</li>
      <br>

</p></ul>
