<h3>What is Brahm Sambandh?</h3>
<br>
<p class="lead">The means to attain God are many. Some are difficult some are easy. All are fairly long journeys.
Bhagwan Shree Krishana states in the Gita that :
<br> <strong>“A minuscule part of Me is present in all living things”</strong>
<br><br>
Every Jiv has <em>Bhagwad ansh;</em> a minuscule part of God. This truth is difficult to comprehend because we do not realise the closeness we have with Thakurji. We have become engrossed in the material world. Our lives are full of activity hence we rarely ask deep rooted questions about who we are, where we are going.<br><br>

On Sharawan Ekadasi Mahaprabhuji along with Damodardas Harsaniji were at Gokul on the banks of Shree Yamunaji. At Govind Ghat (ghat is a area which allows safe access to the river that flows).<br><br>

Shree Mahaprabhuji was perplexed by a thought. How do the divine beings get seva or an opportunity to serve Thakurji in this Kali Yug? More specifically the Vedas do not have a means of liberating Women and Sudhras. Therefore what was to happen of Women, Shudras and non Hindus. This was the question that Shree Mahaprabhuji was worried about.<br><br>

Then at about midnight Shree Nathji appeared (in Lalit trabhangi mudra) and ordained Mahaprabhuji.
The divine souls should take Braham Sambhand. In order to be fit to receive communion with Thakurji their “doshs” will be absolved. <br><br>

Once this is done I (Thakurji) am ready to accept the soul. After Braham Sambhand they should offer everything they partake to me first. Then to take themselves.<br><br>

Therefore everything we take must be offered to Thakurji as already mentioned. This is the the jewel in the Crown and is a very important initiation that many people seldom obtain it!<br><br>

This ceremony makes you realise that relationship that exists between you and Thakurji. When you take this initiation you have to be fairly certain as to what you are doing. As always you have to find a Goswami Balak who will initiate you. <br><br>

<br> The importance of Bhramsambandh lies in the fact that in the ceremony you offer all that belongs to you to the feet of Thakurji. You offer your body, your soul, your wealth, your husband/wife, your children and everything else that belongs to you which would cause you to be attached to objects. All these are offered to Thakurji and you accept His refuge and become a ‘das’. A humble servant of Thakurji. You then humbly bow to Thakurji and offer a token of your gratitude. <br><br>

Later on you Guru will instruct you as to the seva you should perform and jap you should perform. In return you offer your Guru a token of your gratitude for showing you indebtedness and in directing you to the true path to God realisation and opening the door to Thakurji Gaulok.<br><br>

By taking Bhramsambandh you have turned a new leaf, it’s a new beginning your sins have been absolved by Mahaprabhuji and you make a fresh start on the path towards Thakurji. Mahaprabhuji also gives us an assurance that our soul will not have a ‘laukik gati’.</p>


