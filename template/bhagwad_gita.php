							<h3>Shreemad Bhagwad Gita</h3>
							<blockquote>
								<p><strong>कर्मण्येवाधिकारस्ते मा फलेषु कदाचन।	मा कर्मफलहेतुर्भूर्मा ते सङ्गोऽस्त्वकर्मणि।।</p></strong>
										<footer>Shrimad Bhagvad Gita <cite title="Source Title">Chapter 2 : Verse 47</cite></footer>
									</blockquote>
							<p> A person has the right towards action alone and not towards the fruit of action. Let not the fruit of action be the motive for acting. Also, Let there not be any attachment to inaction.
							</p>
							<p>
								The Above verse is widely misunderstood to the extent of refraining people from action. In fact, Lord Kirshna, has stressed the need to action for righteous deeds & dharma at many places. Krishna's intent in the above verse is that we have total control and right of our actions but not on the fruits of the same. <strong></strong>"Karma Phal" </p>- The fruit of our actions is the right of Lord Krishna. In <strong>"Kama Yoga" </strong>- Chapter : 5  Lord Krishna has beautifully explained this by saying :

							<blockquote>
								<p><strong>
								Both renunciation and the yoga of action lead to the supreme good. But of these two, performance of action is superior to the renunciation of action. 
							</blockquote>
							</strong>
							<strong>
							Shrimad Bhagvad Gita, a summation of many aspects of the Vedic, Yogic, Vedantic and Tantric philosophies, is a handbook of self realization and guide to righteous action by every seeker of god.
							<br><br>
							Let this beautiful composition guide us all in the struggle for self-mastery that every human being must wage. 
							</strong>

							</p>




